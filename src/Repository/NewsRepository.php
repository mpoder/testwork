    public function findByCat($cat): array
    {
        return $this->createQueryBuilder('n')
            ->andWhere(':val member of n.categories')
            ->setParameter('val', $cat)
            ->orderBy('n.id', 'ASC')
            ->setMaxResults(10)
            ->getQuery()
            ->getResult()
